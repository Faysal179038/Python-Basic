# type casting - convert the data type of a value to another data type.
x = 1  # int
y = 2.0  # float
z = "3"  # str

x = float(x)
y = int(y)
z = int(z)

print(x)
print(y)
print(z * 3)


x = str(x)
y = str(y)
z = str(z)

print(x)
print(y)
print(z * 3)


# Example: int("123") - converts string "123" to integer 123
